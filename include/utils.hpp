#ifndef UTILS_HPP
#define UTILS_HPP

#define ABS( X ) ( (X) < 0 ? -(X) : (X) )
#define SGN( X ) ( (X) < 0 ? -1 : (X) > 0 ? 1 : 0 )
#define SQR( X ) ( (X) * (X) )
#define MIN( X, Y ) ( (X) < (Y) ? (X) : (Y) )
#define MAX( X, Y ) ( (X) > (Y) ? (X) : (Y) )
#define AVG( X, Y ) ( ( (X) + (Y) ) / 2 )

#endif // UTILS_HPP
 
